// Return true if pixel at absolute canvas (x, y) should be BLACK for this pattern.
export function patternPixel(rows, x, y) {
    return !!((rows[y & 7] >> (7 - (x & 7))) & 1);
}

// Build a repeating CanvasPattern from an 8-byte row array.
// The pattern tiles from (0,0) of the canvas — authentic QuickDraw behaviour.
// fg/bg are [r,g,b] arrays (0-255 each); defaults are black/white.
export function buildPattern(ctx, rows, fg = [0,0,0], bg = [255,255,255]) {
    const off = document.createElement('canvas');
    off.width = 8; off.height = 8;
    const oc = off.getContext('2d');
    const img = oc.createImageData(8, 8);
    for (let y = 0; y < 8; y++) {
        for (let x = 0; x < 8; x++) {
            const on = (rows[y] >> (7 - x)) & 1;
            const i  = (y * 8 + x) * 4;
            const c  = on ? fg : bg;
            img.data[i]   = c[0];
            img.data[i+1] = c[1];
            img.data[i+2] = c[2];
            img.data[i+3] = 255;
        }
    }
    oc.putImageData(img, 0, 0);
    return ctx.createPattern(off, 'repeat');
}
